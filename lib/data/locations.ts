// Helper functions
export const getProvinces = (): string[] => {
  return PAKISTAN_LOCATIONS.map((p) => p.name);
};

export const getCitiesByProvince = (provinceName: string): string[] => {
  const province = PAKISTAN_LOCATIONS.find((p) => p.name === provinceName);
  return province ? province.cities.map((c) => c.name) : [];
};

export const getAreasByCity = (provinceName: string, cityName: string): string[] => {
  const province = PAKISTAN_LOCATIONS.find((p) => p.name === provinceName);
  if (!province) return [];
  
  const city = province.cities.find((c) => c.name === cityName);
  return city ? city.areas.map((a) => a.name) : [];
};
